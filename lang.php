// Helper para traducir una clave
function __($key) {
    global $translations, $current_lang;
    return $translations[$current_lang][$key] ?? $translations['es'][$key] ?? $key;
}

// Helper para renderizar el selector de idioma en HTML
function renderLanguageSelector() {
    global $current_lang;
    $flags = [
        'es' => '🇪🇸 ES',
        'en' => '🇬🇧 EN',
        'it' => '🇮🇹 IT',
        'de' => '🇩🇪 DE',
        'fr' => '🇫🇷 FR'
    ];
    
    // Obtenemos los parámetros GET actuales para no perder filtros
    $params = $_GET;
    
    echo '<div class="lang-selector-container">';
    echo '<select onchange="window.location.href=this.value" class="lang-select">';
    foreach ($flags as $code => $label) {
        $params['lang'] = $code;
        $url = $_SERVER['PHP_SELF'] . '?' . http_build_query($params);
        $selected = ($code === $current_lang) ? 'selected' : '';
        echo "<option value=\"" . htmlspecialchars($url) . "\" $selected>$label</option>";
    }
    echo '</select>';
    echo '</div>';
}

// Helper para exportar traducciones a JS
function renderJSTranslations() {
    global $translations, $current_lang;
    $keys = [
        'details_title',
        'loading',
        'loading_live_stats',
        'no_incidents',
        'error',
        'api_error',
        'live_min',
        'finished',
        'pending'
    ];
    $js_trans = [];
    $js_trans['lang_code'] = $current_lang;
    foreach ($keys as $k) {
        $js_trans[$k] = $translations[$current_lang][$k] ?? $translations['es'][$k] ?? $k;
    }
    echo '<script>window.AppTranslations = ' . json_encode($js_trans, JSON_HEX_TAG | JSON_HEX_AMP | JSON_HEX_APOS | JSON_HEX_QUOT) . ';</script>';
}
